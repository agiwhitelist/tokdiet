const AUTH_MODULE_LINES = [
  '// src/auth/session.js — session + retry helpers',
  "import crypto from 'node:crypto';",
  "import { CONFIG } from '../config/constants.js';",
  '',
  'const TOKEN_BYTES = 24;',
  'const SESSION_TTL_SECONDS = 3600;',
  '',
  'export function newToken() {',
  "  return crypto.randomBytes(TOKEN_BYTES).toString('hex');",
  '}',
  '',
  'export function isExpired(issuedAt, now = Date.now()) {',
  '  return now - issuedAt > SESSION_TTL_SECONDS * 1000;',
  '}',
  'export function computeBackoff(attempt) {',
  '  const base = 200;',
  '  const capped = Math.min(attempt, 6);',
  '  return base * Math.pow(2, capped);',
  '}',
  '',
  'export function rotate(session) {',
  '  session.token = newToken();',
  '  session.issuedAt = Date.now();',
  '  return session;',
  '}',
];
// NOTE: 'export function computeBackoff' is index 14 (0-based) => line 15.
const AUTH_MODULE = withLineNumbers(AUTH_MODULE_LINES);

