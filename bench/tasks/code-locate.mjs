// bench/tasks/code-locate.mjs
//
// CATEGORY: code-locate  (SURVIVABLE)
//
// Each task dumps one or several large SOURCE FILES as junk — either re-pasted
// 3-5 times byte-identical (collapsed by dedup) or delivered as OLD role:'tool'
// read_file results (older ones elided, keepRecent=1). The question asks to
// LOCATE something verifiable about the code: the line a function is defined on,
// the value of a constant, which file imports a module, the name of an exported
// symbol, etc.
//
// HONESTY CONTRACT for this category:
//   * The answerable detail ALWAYS lives in a RECENT message (a final
//     user/assistant turn) OR in the single retained copy of a deduplicated
//     block. The junk that gets compacted away (older tool dumps, earlier
//     duplicate copies) is genuinely irrelevant to the question — it is the same
//     code re-pasted, or unrelated files — so eliding/deduping it cannot change
//     the answer.
//   * A correct compactor keeps the signal (the recent statement, or the kept
//     dedup copy) => the GOVERNED answer matches BASELINE while using far fewer
//     tokens.
//
// Graders are objective: exact line numbers (number-extract), exact constant
// values, exact filenames / symbol names (toLowerCase().includes). Empty and
// wrong answers are rejected.
//
// Plain Node ESM, dependency-free, no network, no imports.

// ---------------------------------------------------------------------------
// Source-file fixtures. These are realistic-looking code blobs. We line-number
// them deterministically so the "what line is X on?" answers are exact and
// stable. We also re-paste / dump them at scale to clear the >=5000-token
// (~20000+ char) bloat floor per task.
// ---------------------------------------------------------------------------

/**
 * Render an array of source lines with a leading "NNN: " 1-based gutter so the
 * question "on what line is X defined?" has a single deterministic answer that
 * is also visible in the dumped text. The gutter makes the fixture read like a
 * real editor / `cat -n` dump.
 */
function withLineNumbers(lines) {
  return lines
    .map((l, i) => String(i + 1).padStart(3, ' ') + ': ' + l)
    .join('\n');
}

/** Repeat a unit string until the result is at least `minChars` characters. */
function inflate(unit, minChars) {
  let out = '';
  let i = 0;
  while (out.length < minChars) {
    out += unit.replace(/__SEQ__/g, String(i));
    i++;
  }
  return out;
}

/** A big block of UNRELATED source/log filler (no needle). Deterministic. */
function filler(minChars, tag = 'fil') {
  return inflate(
    [
      `// ${tag} __SEQ__ — autogenerated shim, do not edit`,
      'function handler__SEQ__(req, res) {',
      '  const ctx = buildContext(req);',
      '  const t0 = Date.now();',
      '  try {',
      '    const out = dispatch(ctx, req.body);',
      '    res.status(200).json(out);',
      '  } catch (err) {',
      '    logger.error("handler__SEQ__ failed", { err });',
      '    res.status(500).json({ error: "internal" });',
      '  } finally {',
      '    metrics.observe("handler__SEQ__", Date.now() - t0);',
      '  }',
      '}',
      '',
    ].join('\n') + '\n',
    minChars,
  );
}

/** Valid OpenAI tool-call + matching tool result, large content. */
function toolPair(callId, fnName, args, resultText) {
  return [
    {
      role: 'assistant',
      content: null,
      tool_calls: [
        { id: callId, type: 'function', function: { name: fnName, arguments: JSON.stringify(args) } },
      ],
    },
    { role: 'tool', tool_call_id: callId, content: resultText },
  ];
}

